#include "libft.h"

void	ft_putnbr(int n)
{
	int num;

	if (n == -2147483648)
	{
		ft_putstr("-2");
		n = 147483648;
	}
	if (n >= 10)
	{
		num = n % 10 + '0';
		n = n / 10;
		ft_putnbr(n);
		ft_putchar(num);
	}
	else if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
		ft_putnbr(n);
	}
	else if (n < 10)
		ft_putchar(n + '0');
}
